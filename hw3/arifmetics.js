// ДВА ЧИСЛА
const a = 10;
const b = 3;

console.log("a + b =", a + b); // 13
console.log("a - b =", a - b); // 7
console.log("a * b =", a * b); // 30
console.log("a / b =", a / b); // ~3.33
console.log("a % b =", a % b); // 1

// РЯДКИ (тексти)
const first = "Hello";
const second = "World";
console.log(first + " " + second); // "Hello World"

// ЧИСЛО + РЯДОК
const num = 5;
const str = "5";
console.log("num + str =", num + str); // "55" (рядок)
console.log("num - str =", num - str); // 0 (число)

// true/false у математиці
console.log("num + true  =", num + true); // 6
console.log("num + false =", num + false); // 5
